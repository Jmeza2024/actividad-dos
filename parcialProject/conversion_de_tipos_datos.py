# Conversión de cadena a decimal
cadena = "123.45"
cadena_a_decimal = float(cadena)
print("Cadena a decimal: {cadena_a_decimal}")

# Conversión de entero a decimal
entero = 10
entero_a_decimal = float(entero)
print(f"Entero a decimal: {entero_a_decimal}")

# Conversión de decimal a entero
decimal = 10.75
decimal_a_entero = int(decimal)
print(f"Decimal a entero: {decimal_a_entero}")
